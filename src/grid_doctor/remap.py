"""Remapping helpers for HEALPix targets.

This module contains the weight-generation and weight-application logic used by
[`regrid_to_healpix`][grid_doctor.remap.regrid_to_healpix].

All remapping is performed via reusable NetCDF weight files generated by
ESMF (in-memory via ESMPy or offline via ``ESMF_RegridWeightGen``) and
applied with SciPy sparse matrices.  Two methods are supported:

- ``"nearest"`` — source-to-destination nearest-neighbour mapping.
- ``"conservative"`` — first-order area-weighted conservative remapping.

The implementation works with regular lon/lat grids, curvilinear lon/lat
grids, and unstructured polygon meshes such as ICON.
"""

from __future__ import annotations

import json
import logging
from pathlib import Path
from typing import Any, cast

import numpy as np
import xarray as xr

from .remap_apply import (
    apply_weights_nd,
    extract_sparse_weights,
)
from .remap_backend import (
    _UNSTRUCTURED_DIMS,
    OfflineWeightConfig,
    _canonical_lon,
    _get_latlon_arrays,
    _get_spatial_dims,
    _get_unstructured_dim,
    _is_unstructured,
    _normalise_angle_units,
    _require_healpix_geo_module,
    compute_healpix_weights_backend,
)
from .types import (
    ApplyBackend,
    FloatArray,
    MissingPolicy,
    RemapMethod,
    SourceKind,
    SourceUnits,
)

logger = logging.getLogger(__name__)


# ===================================================================
# Source-centre coordinate extraction
# ===================================================================


def _source_centre_arrays(
    ds: xr.Dataset,
    *,
    source_units: SourceUnits,
) -> tuple[FloatArray, FloatArray, tuple[str, ...]]:
    """Extract flattened source-centre coordinates and dimension names.

    Args:
        ds: Source dataset.
        source_units: Angular unit convention.

    Returns:
        ``(lat, lon, source_dims)``.
    """
    lat, lon = _get_latlon_arrays(ds)
    lat = _normalise_angle_units(lat, source_units)
    lon = _canonical_lon(_normalise_angle_units(lon, source_units))

    if _is_unstructured(ds):
        spatial_dim = _get_unstructured_dim(ds)
        return lat.ravel(), lon.ravel(), (spatial_dim,)

    y_dim, x_dim = _get_spatial_dims(ds)
    if lat.ndim == 1:
        lon2d, lat2d = np.meshgrid(lon, lat)
        return lat2d.ravel(), lon2d.ravel(), (y_dim, x_dim)
    return lat.ravel(), lon.ravel(), (y_dim, x_dim)


# ===================================================================
# HEALPix cell centres
# ===================================================================


def _healpix_centres(
    level: int,
    *,
    nest: bool,
) -> tuple[FloatArray, FloatArray]:
    """Return HEALPix cell-centre coordinates in degrees.

    All geometry uses a spherical ellipsoid, consistent with ESMF's
    internal area calculation and with climate model coordinates.

    Args:
        level: HEALPix refinement level.
        nest: Nested ordering when *True*, otherwise ring.

    Returns:
        ``(lat_deg, lon_deg)`` arrays.
    """
    module, kwargs = _require_healpix_geo_module(nest)
    ipix = np.arange(12 * (4**level), dtype=np.int64)
    lon_deg, lat_deg = module.healpix_to_lonlat(ipix, level, **kwargs)
    return (
        np.asarray(lat_deg, dtype=np.float64),
        _canonical_lon(np.asarray(lon_deg, dtype=np.float64)),
    )


# ===================================================================
# Weight-file reading (xarray layer on top of remap_apply)
# ===================================================================


def _read_weight_file(
    weights_path: str | Path,
) -> tuple[Any, int, int, int, str, str | None, tuple[str, ...] | None]:
    """Read a weight file and return matrix plus metadata.

    Returns:
        ``(matrix, n_target, n_source, level, order, method,
        stored_source_dims)``.
    """
    with xr.open_dataset(weights_path) as wds:
        row_name = "row" if "row" in wds else "dst_address"
        col_name = "col" if "col" in wds else "src_address"
        val_name = "S" if "S" in wds else "remap_matrix"

        matrix, n_target, n_source = extract_sparse_weights(
            row=wds[row_name].values,
            col=wds[col_name].values,
            values=wds[val_name].values,
        )
        level = int(wds.attrs.get("grid_doctor_level", -1))
        order = str(wds.attrs.get("grid_doctor_order", "nested"))
        method_raw = wds.attrs.get("grid_doctor_method")
        method = str(method_raw) if method_raw is not None else None
        stored_source_dims = _parse_source_dims_attr(
            wds.attrs.get("grid_doctor_source_dims")
        )
    return matrix, n_target, n_source, level, order, method, stored_source_dims


# ===================================================================
# Weight generation facade
